// Combined questions
export const ALL_ALGEBRA_QUESTIONS = [...ALGEBRA_1_QUESTIONS, ...ALGEBRA_2_QUESTIONS];

// Helper functions
export function getAlgebraQuestionsByCourse(course: "Algebra 1" | "Algebra 2"): AlgebraQuestion[] {
  return ALL_ALGEBRA_QUESTIONS.filter(q => q.course === course);
}

export function getAlgebraQuestionsByTopic(course: "Algebra 1" | "Algebra 2", topic: string): AlgebraQuestion[] {
  return ALL_ALGEBRA_QUESTIONS.filter(q => q.course === course && q.topic === topic);
}

export function getAlgebraQuestionsByStandard(standardCode: string): AlgebraQuestion[] {
  return ALL_ALGEBRA_QUESTIONS.filter(q => q.standardCode === standardCode);
}

export function getRandomAlgebraQuestions(course: "Algebra 1" | "Algebra 2", count: number): AlgebraQuestion[] {
  const questions = getAlgebraQuestionsByCourse(course);
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function getAlgebraTopics(course: "Algebra 1" | "Algebra 2"): string[] {
  const questions = getAlgebraQuestionsByCourse(course);
  return [...new Set(questions.map(q => q.topic))];
}
